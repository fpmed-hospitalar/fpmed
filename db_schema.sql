-- VIEWS usadas pelo app
create or replace view public.v_compras_mensal as
  select cliente_codigo,
         to_char(data_compra,'YYYY-MM') as ano_mes,
         count(*)          as num_compras,
         sum(valor_total)  as total_mes
  from public.compras
  where data_compra is not null
  group by cliente_codigo, to_char(data_compra,'YYYY-MM');

-- v_preco_variacao: stub com as colunas certas (retorna vazio ate haver historico/logica real)
create or replace view public.v_preco_variacao as
  select null::text    as fornecedor,
         null::text    as produto_norm,
         null::numeric as var_pct,
         null::numeric as preco_atual,
         null::numeric as preco_anterior,
         null::date    as data_anterior
  where false;

