-- CLIENTES (cnpj_normalizado GERADA + unique; codigo identity unico p/ FK)
create table if not exists public.clientes (
  id                bigint generated always as identity primary key,
  codigo            bigint generated by default as identity,
  nome              text,
  cnpj              text,
  cnpj_normalizado  text generated always as (regexp_replace(coalesce(cnpj,''), '\D', '', 'g')) stored,
  cod_cliente_erp   text,
  vendedora         text,
  contato           text,
  whatsapp          text,
  email             text,
  cidade            text,
  uf                text,
  obs               text,
  criado_em         timestamptz default now(),
  atualizado_em     timestamptz default now()
);
do $$ begin
  if not exists (select 1 from pg_constraint where conname='clientes_codigo_key') then
    alter table public.clientes add constraint clientes_codigo_key unique (codigo);
  end if;
  if not exists (select 1 from pg_constraint where conname='clientes_cnpjnorm_key') then
    alter table public.clientes add constraint clientes_cnpjnorm_key unique (cnpj_normalizado);
  end if;
  if not exists (select 1 from pg_constraint where conname='clientes_coderp_key') then
    alter table public.clientes add constraint clientes_coderp_key unique (cod_cliente_erp);
  end if;
end $$;

